def test_take_profit_no_entry_price(monitor_tp, mock_handler):
    tracker = MagicMock()
    # Position without entry price
    pos = Position(ticker="AAPL", weight=0.5, entry_price=None, entry_date=pd.Timestamp("2020-01-01"))
    tracker.current_positions = {"AAPL": pos}
    
    # Handler should receive NaN for entry price
    mock_handler.calculate_take_profit_levels.return_value = pd.Series({"AAPL": 150.0})
    mock_handler.apply_take_profit.return_value = pd.Series({"AAPL": 0.0}) # Liquidate
    
    current_date = pd.Timestamp("2020-01-02")
    monitor_tp.check_positions_for_take_profit(
        current_date, tracker, pd.Series({"AAPL": 140.0}), mock_handler, pd.DataFrame()
    )
    
    # Verify handler call arguments
    call_args = mock_handler.calculate_take_profit_levels.call_args
    entry_prices_arg = call_args[1]["entry_prices"]
    assert np.isnan(entry_prices_arg["AAPL"])
