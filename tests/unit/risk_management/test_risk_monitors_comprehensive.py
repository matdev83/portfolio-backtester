def test_stop_loss_reset_monitoring_state(monitor_sl):
    monitor_sl.triggered_positions = {"AAPL": pd.Timestamp("2020-01-01")}
    monitor_sl.last_check_date = pd.Timestamp("2020-01-01")
    
    monitor_sl.reset_monitoring_state()
    
    assert not monitor_sl.triggered_positions
    assert monitor_sl.last_check_date is None

def test_stop_loss_simultaneous_triggers(monitor_sl, mock_tracker, mock_handler):
    current_date = pd.Timestamp("2020-01-02")
    current_prices = pd.Series({"AAPL": 90.0, "MSFT": 180.0}) # Both dropped
    
    # Handler returns weights where BOTH are liquidated (0.0)
    mock_handler.apply_stop_loss.return_value = pd.Series(
        {"AAPL": 0.0, "MSFT": 0.0}, index=["AAPL", "MSFT"]
    )
    
    result = monitor_sl.check_positions_for_stop_loss(
        current_date, mock_tracker, current_prices, mock_handler, pd.DataFrame()
    )
    
    # Should contain columns for both, value 0.0
    assert "AAPL" in result.columns
    assert "MSFT" in result.columns
    assert result.loc[current_date, "AAPL"] == 0.0
    assert result.loc[current_date, "MSFT"] == 0.0
    
    assert "AAPL" in monitor_sl.triggered_positions
    assert "MSFT" in monitor_sl.triggered_positions

def test_stop_loss_partial_liquidation_unsupported(monitor_sl, mock_tracker, mock_handler):
    # Monitor logic assumes if weight changes to 0 -> liquidated.
    # What if weight changes from 0.5 to 0.25 (partial stop)?
    # _identify_liquidated_positions checks: is_closed = (weights_after == 0)
    # So partial liquidation is NOT treated as a "trigger" event in the current monitor logic
    # that sets 'triggered_positions'.
    
    current_date = pd.Timestamp("2020-01-02")
    current_prices = pd.Series({"AAPL": 90.0, "MSFT": 200.0})
    
    # AAPL reduced to 0.25
    mock_handler.apply_stop_loss.return_value = pd.Series(
        {"AAPL": 0.25, "MSFT": 0.5}, index=["AAPL", "MSFT"]
    )
    
    result = monitor_sl.check_positions_for_stop_loss(
        current_date, mock_tracker, current_prices, mock_handler, pd.DataFrame()
    )
    
    # Since no position became 0, liquidated_positions should be empty
    # And thus result should be empty (no liquidation signals generated by monitor)
    # The monitor is designed for FULL liquidation signals primarily.
    assert result.empty
    assert "AAPL" not in monitor_sl.triggered_positions

